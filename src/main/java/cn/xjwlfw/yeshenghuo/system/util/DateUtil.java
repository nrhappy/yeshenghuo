package cn.xjwlfw.yeshenghuo.system.util;

import java.text.SimpleDateFormat;
import java.util.Date;

/**
 * 
 * 时间工具
 * 
 * @author lee
 * @since 2017/2/17 10:50:00
 *
 */
public class DateUtil {
	
	/**
	 * 获取当前时间
	 * 
	 * @return 当前时间格式化后的字符串 示例: "2000-10-0 18:00:00"
	 */
	public static String getCurrentTime() { return new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date()); }
	
	/**
	 * 获取当前格式化时间
	 * 
	 * @param pattern - 时间格式 示例: "yyyy-MM-dd HH:mm:ss"
	 * @return 当前时间格式化后的字符串 示例: "2000-10-0 18:00:00"
	 */
	public static String getCurrentTime(String pattern) { return new SimpleDateFormat(pattern).format(new Date()); }
}